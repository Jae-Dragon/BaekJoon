#include <stdio.h>


int main()
{
    int N;
    scanf("%d", &N);

    /* 수열의 규칙: 0을 쓸 떄는 00만 가능 */
    /* i번쨰 dp는 i-2번째에서 */
    long long dp[1000001];
    
    dp[1] = 1;// 1
    dp[2] = 2;// 00, 11
    dp[3] = 3;// 001, 100, 111
    dp[4] = 5;// 0000, 0011, 1100, 1111 , 1001
    dp[5] = 8;// 10000, 00001, 00111, 11100, 11001, 11111, 10011
 
    
    for(int i = 3; i <= N; i++)
    {
        dp[i] = (dp[i-2] + dp[i-1]) % 15746;
    }

    printf("%lld", dp[N]);

    return 0;
}